def get_persona_by_language(language: str) -> str:
    """
    Returns the AiSyster persona prompt for the specified language.

    Args:
        language: Language code ('en', 'pt', 'es')

    Returns:
        The persona prompt string
    """
    prompts = {
        'en': AISYSTER_PERSONA_EN,
        'pt': AISYSTER_PERSONA_PT,
        'es': AISYSTER_PERSONA_ES
    }
    return prompts.get(language, AISYSTER_PERSONA_EN)  # Default to English


